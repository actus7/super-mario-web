// Simple Map Generation (1-1 ish)
// 200 tiles wide
export const GENERATE_LEVEL = () => {
  const width = 220;
  const height = 15;
  const map: number[][] = [];

  for (let y = 0; y < height; y++) {
    const row: number[] = [];
    for (let x = 0; x < width; x++) {
      // Floor
      if (y > 12) {
        if (x > 68 && x < 71) row.push(0); // Pit 1
        else if (x > 85 && x < 88) row.push(0); // Pit 2
        else if (x > 152 && x < 154) row.push(0); // Pit 3
        else row.push(1); // Ground
      } else {
        row.push(0); // Air
      }
    }
    map.push(row);
  }

  const addBlock = (x: number, y: number, type: number) => {
     if(y >= 0 && y < height && x >= 0 && x < width) map[y][x] = type;
  };

  const addPipe = (x: number, h: number) => {
    for(let i=0; i<h; i++) {
       addBlock(x, 12-i, 5);
       addBlock(x+1, 12-i, 6);
    }
    addBlock(x, 12-h, 7);
    addBlock(x+1, 12-h, 8);
  }

  // Structures
  // QBlocks and Bricks
  addBlock(16, 9, 4);
  addBlock(20, 9, 2); addBlock(21, 9, 4); addBlock(22, 9, 2); addBlock(23, 9, 4); addBlock(24, 9, 2);
  addBlock(22, 5, 4);
  
  addPipe(28, 1);
  addPipe(38, 2);
  addPipe(46, 3);
  addPipe(57, 3);

  addBlock(77, 9, 2); addBlock(78, 9, 4); addBlock(79, 9, 2);
  
  // Stairs
  for(let i=0; i<4; i++) {
      for(let j=0; j<=i; j++) addBlock(134+i, 12-j, 3);
      for(let j=0; j<=i; j++) addBlock(143-i, 12-j, 3);
  }
  
  // Castle ish
  addBlock(198, 12, 2); 
  
  return map;
};
